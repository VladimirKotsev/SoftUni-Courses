function operation(a, b, iterator){
    if (iterator == '+')
    {
        console.log(a + b);
    }
    else if (iterator == '-')
    {
        console.log(a - b);
    }
    else if (iterator == '*')
    {
        console.log(a * b);
    }
    else if (iterator == '/')
    {
        console.log(a / b);
    }
    else if (iterator == '%')
    {
        console.log(a % b);
    }
    else if (iterator == "**")
    {
        console.log(a**b);
    }
}
